"""
Question:

With two given lists [1,3,6,78,35,55] and [12,24,35,24,88,120,155], write a program to make a list whose elements are intersection of the above given lists.
"""

li1 = [1,3,6,78,35,55]

li2 = [12,24,35,24,88,120,155]

s1 = set(li1)

s2 = set(li2)

s1 &= s2

