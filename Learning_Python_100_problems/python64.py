"""
Question:

Write a program to compute 1/2+2/3+3/4+...+n/n+1 with a given n input by console (n>0).
"""

def computer(n):
    if n>1:
        return (n/float(n+1))+(computer(n-1))
    elif n<1:
        return 0
    else:
        return (1/2.0)
        
